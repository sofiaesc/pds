N = 20;
t=0:1:99;
x = zeros(1,length(t));
x(1)=1;

%-------------------------------------%
%--------------INCISO 1---------------%
y_1 = zeros(1,100);
y_1(1) = x(1);
y_1(2) = x(2)-1/2*y_1(1);
for i = 3:100
  y_1(n) = x(n)-1/2*y_1(n-1)-1/4*y_1(n-2);
endfor

%-------------------------------------%
%--------------INCISO 2---------------%
y_2 = zeros(1,100);
y_2(1) = 0;
y_2(2) = y_2(1) + x(1);
y_2(3) = y_2(2) + y_2(1) + x(2);
for n = 3:100
  y_2(n) = y_2(n-1) + y_2(n-2) + x(n-1);
endfor

%-------------------------------------%
%--------------INCISO 3---------------%
y_3 = zeros(1,100);
y_3(1) = 7*x(1);
y_3(2) = 7*x(2) + 2*y_3(1);
for n = 3:100
  y_3(n) = 7*x(n) + 2*y_3(n-1) - 6*y_3(n-2);
endfor

%-------------------------------------%
%--------------INCISO 4---------------%
y_4 = zeros(1,100);
for n = 1:100
  for k = 1:7
    aux = n-k;
    if(aux >= 1)
      y_4(n) += 2^-k*x(aux);
    endif
  endfor
endfor


